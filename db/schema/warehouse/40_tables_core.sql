    asset_kind TEXT NOT NULL,
    asset_url TEXT NOT NULL,
    content_type TEXT,
    availability_raw TEXT,
    asset_checksum TEXT,
    PRIMARY KEY (paper_id, asset_kind, asset_url)
);
